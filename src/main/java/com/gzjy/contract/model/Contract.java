package com.gzjy.contract.model;

import java.util.Date;

public class Contract {

	private String id;
	private String company;
	private String member;
	private String phone;
	private Date time;
	private String sampleName;
	private String count;
	private String testBy;
	private String demandPeriod;
	private String testItem;
	private String extra;
	public String getId() {
		return id;
	}
	public void setId(String id) {
		this.id = id;
	}
	public String getCompany() {
		return company;
	}
	public void setCompany(String company) {
		this.company = company;
	}
	public String getMember() {
		return member;
	}
	public void setMember(String member) {
		this.member = member;
	}
	public String getPhone() {
		return phone;
	}
	public void setPhone(String phone) {
		this.phone = phone;
	}
	public Date getTime() {
		return time;
	}
	public void setTime(Date time) {
		this.time = time;
	}
	public String getSampleName() {
		return sampleName;
	}
	public void setSampleName(String sampleName) {
		this.sampleName = sampleName;
	}
	public String getCount() {
		return count;
	}
	public void setCount(String count) {
		this.count = count;
	}
	public String getTestBy() {
		return testBy;
	}
	public void setTestBy(String testBy) {
		this.testBy = testBy;
	}
	public String getDemandPeriod() {
		return demandPeriod;
	}
	public void setDemandPeriod(String demandPeriod) {
		this.demandPeriod = demandPeriod;
	}
	public String getTestItem() {
		return testItem;
	}
	public void setTestItem(String testItem) {
		this.testItem = testItem;
	}
	public String getExtra() {
		return extra;
	}
	public void setExtra(String extra) {
		this.extra = extra;
	}
	
	
	
	
	
}
